public class Cars {
    private int year;
    private int hp;
    private int model;

    public Cars(int year, int hp, int model) {
        this.year = year;
        this.hp = hp;
        this.model = model;
    }

    public int getYear() {
        return year;
    }

    public int getHp() {
        return hp;
    }

    public int getModel() {
        return model;
    }

}
